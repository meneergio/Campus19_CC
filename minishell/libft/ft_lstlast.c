#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	while (lst && lst->next)
		lst = lst->next;
	return (lst);
}
/* doorloopt de lijst tot het laatste element wordt bereikt
retourneert pointer naar de laatste node*/
