#include "libft.h"

void	ft_lstadd_front(t_list **lst, t_list *new)
{
	if (lst && new)
	{
		new->next = *lst;
		*lst = new;
	}
}
/*  Sets the next pointer of the new node to the current first node
    Sets the list's head pointer to the new node */
